IconData getNavbarIconByName({required String route, required bool isActive}) {
  switch (route) {
    case '/search':
      return isActive ? KronkIcon.searchFill1 : KronkIcon.searchOutline1;
    case '/feeds':
      return isActive ? KronkIcon.quillFill : KronkIcon.quillOutline;
    case '/chats':
      return isActive ? KronkIcon.messageCircleLeftFill : KronkIcon.messageCircleLeftOutline;
    case '/groups':
      return isActive ? KronkIcon.usersFill : KronkIcon.usersOutline;
    case '/education':
      return isActive ? KronkIcon.rulerPenFill : KronkIcon.rulerPenOutline;
    case '/notes':
      return isActive ? KronkIcon.note1Fill : KronkIcon.note1Outline;
    case '/vocabulary':
      return isActive ? KronkIcon.dictionaryFill1 : KronkIcon.dictionaryOutline1;
    case '/translator':
      return isActive ? KronkIcon.translatorFill1 : KronkIcon.translatorOutline1;
    case '/todos':
      return isActive ? KronkIcon.taskFill : KronkIcon.taskOutline;
    case '/entertainment':
      return isActive ? KronkIcon.playerFill : KronkIcon.playerOutline;
    case '/profile':
      return isActive ? KronkIcon.profileFill : KronkIcon.profileOutline;

    default:
      return Icons.error_outline_rounded;
  }
}
